package cn.com.sony.csc.sqa.mbt.bean;

import java.io.Serializable;

import cn.com.sony.csc.sqa.mbt.constant.Command;

public class CommandBean implements Serializable {

	private static final long serialVersionUID = 5773286791139762996L;

	private int executeId; //Execute id
	private int executeTestCaseId; // ExecuteTestCase id
	private int executeStatementId; // ExecuteStatement id
	private Command command;

	public int getExecuteId() {
		return executeId;
	}

	public void setExecuteId(int executeId) {
		this.executeId = executeId;
	}

	public int getExecuteTestCaseId() {
		return executeTestCaseId;
	}

	public void setExecuteTestCaseId(int executeTestCaseId) {
		this.executeTestCaseId = executeTestCaseId;
	}

	public int getExecuteStatementId() {
		return executeStatementId;
	}

	public void setExecuteStatementId(int executeStatementId) {
		this.executeStatementId = executeStatementId;
	}

	public Command getCommand() {
		return command;
	}

	public void setCommand(Command command) {
		this.command = command;
	}

}
